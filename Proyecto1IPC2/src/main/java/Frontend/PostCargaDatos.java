    private void btnBuscarDatActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscarDatActionPerformed
        // TODO add your handling code here:
                try {
            JFileChooser file_Chooser = new JFileChooser();
            file_Chooser.addChoosableFileFilter(new FileNameExtensionFilter("Archivo de texto", "txt")); //solo se buscara archivos con extension .txt
            file_Chooser.setAcceptAllFileFilterUsed(false);
            int seleccion = file_Chooser.showOpenDialog(this);
            
            if (seleccion == APPROVE_OPTION) {
                //System.out.println("Path: " + fileChooser.getSelectedFile().getPath());
                RCargaDatos read_datos = new RCargaDatos();                
                read_datos.readDatos(file_Chooser.getSelectedFile().getPath());
                this.dispose();
            } 
        } catch (HeadlessException e) {
            JOptionPane.showMessageDialog(this, e.getMessage(), "Info", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btnBuscarDatActionPerformed

