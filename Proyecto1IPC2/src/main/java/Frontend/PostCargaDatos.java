    private void cargarListMal(){        
        while  (error_registro.peek() != null){
            String data = (String) error_registro.poll();
            lista_error.addElement(data);
        }        
        
    }
    private void cargarListBien(){
        while  (bien_registro.peek() != null){
            String data = (String) bien_registro.poll();
            lista_bien.addElement(data);
        }
    }
    

