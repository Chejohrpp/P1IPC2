    //Verificamos que el empleado exista y llenamos variables globales
    private boolean verificarIngreso(String cod,String nombre){
        ConnectionDB con = new ConnectionDB();
        Connection connection = con.getConnection();
        String query = "SELECT COUNT(*) FROM empleado Where nombre= ? and codigo = ?";
        try(PreparedStatement preSt = connection.prepareStatement(query)){
            preSt.setString(1, nombre);
            preSt.setString(2, cod);
            ResultSet result = preSt.executeQuery();
            if (result.next()) {
                if (Integer.parseInt(result.getString(1)) == 1) {
                    emp_actual = cod;                     
                    con.closeConnection();
                    return true;
                }
            }             
            con.closeConnection();
        }catch(SQLException e){
            System.out.println(e.getMessage());
        }
        return false;
    }
