    //llenamos el JcomboBox con los nombres de la tienda
    private void llenarCmb(){
        String query = "SELECT nombre FROM tienda";
        try(PreparedStatement preSt = connection.prepareStatement(query)){
            ResultSet result = preSt.executeQuery();
            while(result.next()){
                cmbTienda.addItem(result.getString(1));
            }
            result.close();
            con.closeConnection();
        }catch(SQLException e){
            
        }
    }
    //buscamos el codigo de la tienda
    private void codTienda(String nombre){
        ConnectionDB con = new ConnectionDB();
        Connection connection = con.getConnection();
        String query = "SELECT codigo from tienda WHERE nombre= ?";
        try(PreparedStatement preSt = connection.prepareStatement(query)){
            preSt.setString(1, nombre);
            
            ResultSet result = preSt.executeQuery();            
            
            if (result.next()) {
                tienda_actual = result.getString(1);
                //System.out.println(tienda_actual);
            } 
            result.close();
            con.closeConnection();
        }catch(SQLException e){
            System.out.println(e.getMessage());
        }
    }
    
