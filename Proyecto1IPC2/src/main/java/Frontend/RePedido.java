    private void btnRegistarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRegistarActionPerformed
        // TODO add your handling code here:
        try{
            int tiempo = 1;
            int cod = Integer.parseInt(txtCodPedido.getText());            
            if (tbtnSI.isSelected()) {
                tiempo = 1;
            }else{
                tiempo = 0;
            }
            
            ModDB mod = new ModDB();
            mod.RePedido(this, cod, tiempo);
        }catch(Exception e){
            JOptionPane.showMessageDialog(null, "Ingrese bien el codigo del pedido", "ERORR", JOptionPane.ERROR_MESSAGE); 
        }
    }//GEN-LAST:event_btnRegistarActionPerformed

