    private void btnCofirmActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCofirmActionPerformed
        // TODO add your handling code here:
        try{
            double quitCred = Double.parseDouble(txtCred.getText());
            double efect = Double.parseDouble(txtEFect.getText());
            double ant = quitCred+efect;
            
            if (quitCred <= cred) {
                if (ant >= anticipo) {
                    AgregarDB agregar = new AgregarDB();  
                    agregar.addPedido(cmbTienda.getSelectedItem().toString(), tienda_actual, dias, cmbClient.getSelectedItem().toString(), total, ant);
                     ConsultasVarias consulta = new ConsultasVarias();
                     int cod_ped = consulta.lastPedido();
                    while(cod.peek() != null){
                        AgregarDB agregar1 = new AgregarDB();                          
                        agregar1.addPedidoProducto((String) cod.poll(), cod_ped, (int) cant.poll(), (int) exisst.poll(), cmbTienda.getSelectedItem().toString());
                    }                    
                    ModDB mod = new ModDB();
                    mod.modCred(cred-quitCred, cmbClient.getSelectedItem().toString());
                    
                    //JOptionPane.showMessageDialog(null, "Pedido confirmado", "Finalizado Exitoso", JOptionPane.INFORMATION_MESSAGE);
                    OpcionEmp emp = new OpcionEmp();
                    emp.setLocationRelativeTo(null);
                    emp.setVisible(true);
                    this.dispose();
                }else{
                    JOptionPane.showMessageDialog(null, "el anticipo es menor al minimo", "ERORR", JOptionPane.ERROR_MESSAGE);
                }        
            }else{
                JOptionPane.showMessageDialog(null, "supera el limite de uso en el credito", "ERORR", JOptionPane.ERROR_MESSAGE);
            }
        }catch(Exception e){
            System.out.println(e.getMessage());
            JOptionPane.showMessageDialog(null, "no se ha podido completar la tarea, verifique los datos", "ERORR", JOptionPane.ERROR_MESSAGE);  
        }
    }//GEN-LAST:event_btnCofirmActionPerformed


