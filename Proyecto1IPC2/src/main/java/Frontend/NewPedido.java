    private void agregarTienda(){
        ConsultasVarias consulta = new ConsultasVarias();
        LinkedList tiendas = consulta.codTiendas(tienda_actual);
        while(tiendas.peek() != null){
            String data = (String) tiendas.poll();
            cmbTienda.addItem(data);
           
        }
    }
    private void agregarProd(){
        cmbProd.removeAllItems();
        ConsultasVarias consulta = new ConsultasVarias();
        LinkedList productos = consulta.codProds(cmbTienda.getSelectedItem().toString());
        while(productos.peek() != null){
            String data = (String) productos.poll();
            cmbProd.addItem(data);
        }
    }
    private void agregarNIt(){
        cmbClient.removeAllItems();
        ConsultasVarias consulta = new ConsultasVarias();
        LinkedList nit = consulta.nitClient();
        while(nit.peek() != null){
            String data = (String) nit.poll();
            cmbClient.addItem(data);
        }
    }
    private void agregarRes(){
        ConsultasVarias consulta = new ConsultasVarias();
        lblExist.setText(String.valueOf("Existencias: "+consulta.exist(cmbTienda.getSelectedItem().toString(), cmbProd.getSelectedItem().toString())));
        dias = consulta.fechaE(cmbTienda.getSelectedItem().toString());
        lblDate.setText(String.valueOf("Tiempo en dias estimado: "+dias));
        cred = consulta.cred(cmbClient.getSelectedItem().toString());
        lblCred.setText(String.valueOf("Credito m√°ximo del cliente: "+cred));
        lblPrecio.setText("Precio: "+String.valueOf(consulta.precio(cmbProd.getSelectedItem().toString())));
    }
    
    private void generar(){        
        //Agregar listado
        try{
            ConsultasVarias consulta = new ConsultasVarias();
             int cantidad = Integer.parseInt(txtCant.getText());
             int exist = consulta.exist(cmbTienda.getSelectedItem().toString(), cmbProd.getSelectedItem().toString());
            if (cantidad <= exist) {
                cod.offer(cmbProd.getSelectedItem().toString());          
                cant.offer(cantidad);
                int rest = exist-cantidad;
                exisst.offer(rest);
                calc(cantidad,consulta.precio(cmbProd.getSelectedItem().toString()));
                String data[] = {cmbProd.getSelectedItem().toString(),String.valueOf(consulta.precio(cmbProd.getSelectedItem().toString())),String.valueOf(cantidad)};
                dfm.addRow(data);
                cmbTienda.setEnabled(false);
            }else{
                JOptionPane.showMessageDialog(null, "No hay muchas existencias", "ERORR", JOptionPane.ERROR_MESSAGE);
            }
            
        } catch(Exception e){
           JOptionPane.showMessageDialog(null, "Ingrese los datos", "ERORR", JOptionPane.ERROR_MESSAGE);  

        }      
        
    }
    
    private void calc(int cant, double precio){
        total += cant*precio;
        anticipo = total*0.25;
        
        lblTotal.setText("Total: " + String.valueOf(total));
        lblAnt.setText("anticipo minimo: " + String.valueOf(anticipo));
    }
    
    
